export type RawFoodRow = {
  groupId: number;
  foodId: number;
  indexId: number;
  foodName: string;
  // その他多数の栄養フィールドが number | null として続く
  [key: string]: number | string | null;
};

export type ParsedFood = {
  code: string; // 5 桁ゼロ埋め: "01001"
  name: string;
  /** ユーザーが入力する一般的な表記から foods.id を引けるようにする別名集合。
   *  食品成分表の正式名称（例: "(たまねぎ類) たまねぎ りん茎 生"）はそのままでは
   *  漢字混じりの一般名（"玉ねぎ"）と一致しないため、parse 時にここを補強する。 */
  aliases: string[];
  category: FoodCategory;
  food_group: string;
  nutrition_per_100g: Record<string, number | null>;
  source: string;
};

